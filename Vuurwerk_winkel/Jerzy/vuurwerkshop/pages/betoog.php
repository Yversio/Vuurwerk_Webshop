<h1>Nederlands:</h1>
<p><b>Wel / Niet vuurwerk betoog</b><br>
    <b>Mijn mening:</b><br>
    Persoonlijk vind ik vuurwerk niet meer bijzonder, en vind ik dat het verboden moet worden tegenwoordig wordt er alleen maar dingen gesloopt en gehandeld in illegaal vuurwerk dus dat probleem pak je ook aan als vuurwerk verboden wordt in heel Nederland
<br><br>
    <b>Waarom moet het verboden worden?</b><br>
  Omdat er met vuurwerk  alleen maar dingen mee worden gesloopt zoals prullenbakken, bushokjes, deuren, auto’s dit heeft zeker te maken met het illegale vuurwerk uit België Duitsland en Polen. Ook worden er vaak dieren slachtoffer van vuurwerk omdat mensen vuurwerk naar de dieren gooien. Huisdieren zijn meestal ook bang voor vuurwerk.  En ieder jaar zijn er weer heel veel mensen slachtoffer van vuurwerk mensen die hun oog kwijt raken of hun hand door het vuurwerk.
    <br><br>
    <b>De oplossing</b><br>
    In Nederland moet in elke stad een vuurwerk show worden gegeven door professionals wat door de gemeente word georganiseerd zodat alles veilig verloopt en er geen gewonden meer vallen op oudejaarsavond en iedereen gelukkig en gezond het nieuwejaar ik kan gaan.

